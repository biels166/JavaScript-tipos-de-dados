console.log("Gabriel" === "Gabriel");
console.log((30 / 2) === 15);
console.log("15" === 15);

console.log("\nComparações com !=");
console.log("Gabriel" != "Gabriel");
console.log((30 / 2) != 15);
console.log(16 != 15);
