console.log("Comparações com >");
console.log(5 > 10);
console.log((30 / 2) > 5);

console.log("\nComparações com <");
console.log(5 < 10);
console.log((30 % 2) < 5);

console.log("\nComparações com ==");
console.log("Gabriel" == "Gabriel");
console.log((30 / 2) == 15);
console.log("15" == 15);
console.log(20 == 15);

console.log("\nComparações com ===");
console.log("Gabriel" === "Gabriel");
console.log((30 / 2) === 15);
console.log("15" === 15);

console.log("\nComparações com !=");
console.log("Gabriel" != "Gabriel");
console.log((30 / 2) != 15);
console.log(16 != 15);

console.log("\nOperador AND (&&)");
console.log(((1 + 1) == 2) && (5 > 1));
console.log(true && false);

console.log("\nOperador OR ||");
console.log(((1 + 1) == 2) || (5 == 1));
console.log(true || false);

console.log("\nOperador NOT !");
console.log(!(((1 + 1) == 2) || (5 == 1)));
console.log(!(true || false));
console.log(!(((1 + 1) == 2) && (5 > 1)));
console.log(!(true && false));
